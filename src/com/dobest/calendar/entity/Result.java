package com.dobest.calendar.entity;

public class Result {
	
	private String year;
	private String month;
	private String day;
	private String week;
	private String lunaryear;
	private String lunarmonth;
	private String lunarday;
	private String ganzhi;
	private String shengxiao;
	private String star;
	private HuangLi huangli;
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getWeek() {
		return week;
	}
	public void setWeek(String week) {
		this.week = week;
	}
	public String getLunaryear() {
		return lunaryear;
	}
	public void setLunaryear(String lunaryear) {
		this.lunaryear = lunaryear;
	}
	public String getLunarmonth() {
		return lunarmonth;
	}
	public void setLunarmonth(String lunarmonth) {
		this.lunarmonth = lunarmonth;
	}
	public String getLunarday() {
		return lunarday;
	}
	public void setLunarday(String lunarday) {
		this.lunarday = lunarday;
	}
	public String getGanzhi() {
		return ganzhi;
	}
	public void setGanzhi(String ganzhi) {
		this.ganzhi = ganzhi;
	}
	public String getShengxiao() {
		return shengxiao;
	}
	public void setShengxiao(String shengxiao) {
		this.shengxiao = shengxiao;
	}
	public String getStar() {
		return star;
	}
	public void setStar(String star) {
		this.star = star;
	}
	public HuangLi getHuangli() {
		return huangli;
	}
	public void setHuangli(HuangLi huangli) {
		this.huangli = huangli;
	}
	
}
